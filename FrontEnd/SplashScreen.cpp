int ChoosingMenu(int x, int firstline, int lastline)
{
	int v = firstline - 1;
	char a = 175;
	while (true)
	{
		if (_kbhit())
		{
			char c = _getch();
			if (c == 'P' || c == 'S' || c == 's') //down
			{
				gotoXY(x - 2, v);
				cout << (' ');
				if (v == lastline)
				{
					v = firstline - 1; // truoc dong dau tien cua menu (co dinh)
				}
				v = v + 1;
				gotoXY(x - 2, v);
				cout << a;
			}
			if (c == 'w' || c == 'W' || c == 'H') //up
			{
				gotoXY(x - 2, v);
				cout << (' ');
				if (v <= firstline)
				{
					v = lastline + 1;
				}
				v = v - 1;
				gotoXY(x - 2, v);
				cout << a;
			}
			if (c == 13) // 13 la ma ascii cua enter
			{
				return v;
			}
		}
	}
}

